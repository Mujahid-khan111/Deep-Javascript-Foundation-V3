/* JavaScript mein "Kind of Emptiness" ek specific term nahi hai,
 yeh jyadatar subjective feeling ya expression hota hai jo vyakti ke anubhavon ko darust karne ke liye istemal hota hai.
 Yadi aap kisi specific context mein "Kind of Emptiness" ko JavaScript ke sath use karna chahte hain, 
to aapko apne context ke anusar vyakt karna hoga.
 Ek example ke roop mein, 
agar aap ek variable mein kuch store karte hain aur fir use khali kar dete hain,
 to aap uss variable ko "kind of emptiness" ke roop mein describe kar sakte hain:*/


let myVariable = "Some value";
console.log(myVariable);      // Output: Some value

myVariable = "";             // Emptying the variable
console.log(myVariable);     // Output: (Kind of Emptiness)


let stringValue = "Hello, World!";
